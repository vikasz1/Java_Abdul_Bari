public class Chapter2Literals {
    public static void main(String[] args) {
        byte b = 10;//Integer literal stored = 10
        byte c = 0x12;// Hexadecimal literal will be stored = 18
        byte d = 0b1000; // binary literal stored=8
        byte e = 010;
        double f = 4.5678432431423412d;
        System.out.println(f);
        System.out.println(e);
    }
}
